package week_3.day_1.activities;

public class WorkingWithOperators {

    public static void main(String[] args) {

        System.out.println( 5 / 4 );
        double result = 5 / 4;

        System.out.println( result ); // 1.0

        int intResult = 5 / 4;

        System.out.println( intResult );


        System.out.println( 5 % 4 );

        System.out.println( " * * * * * *  * * * * * *  * * * * * *  * * * * * *  * * * * * *  " );

        int intNumber = 10;
        float doubleNumber = 10;

        System.out.println( intNumber ); // 10
        System.out.println( doubleNumber ); // 10.0




        System.out.println( " * * * * * *  * * * * * *  * * * * * *  * * * * * *  * * * * * *  " );

        System.out.println("Hello Commanders" + 10 + 10 + (50 % 10 - 2) + "Java!");













    }

}
