package week_2.day_2.activities;

public class NamingVariables {

    public static void main(String[] args) {

        String firstName = "Tony";
        int examScore = 56;

        // Names of the variable cannot be started with numbers
        String lastName;

        // variable names cannot be started with special characters except "_" and "$"
        String $middleName;

        int _salary = 120_000_00;

        System.out.println( _salary );

        // _salary = $120000;

       //  String full_?Name;

        int age;
        int Age;
        int aGe;
        int agE;

    }

}
